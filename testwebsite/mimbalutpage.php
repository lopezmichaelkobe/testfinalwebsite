      <h3>About Mimbalot Falls</h3><br>
      <p>Mimbalot Falls is known as one of the most prestigious waterfalls in Iligan City, paired with Tinago and Maria Cristina Falls altogehter.
        It is a great tourist attraction worth visiting, especially for nature enthusiasts.
        <br><br>
        It is part of the <i>Iligan Paradise Resort and Eco Park</i>, to which the guests can also enjoy the zipline and get a clearer view of the waterfalls.
        Mimbalot is considered to be one of the most accessible waterfalls in Lanao del Norte. Inside, you can access the cable car and feast your eyes to the scenic route. 
        You can also get your pleasure from tours across the resort using the all-terrain vehicles provided by the resort for a reasonable amount, as well as dirt bikes.<br>
        <br>The resort also features the concrete tables and chairs for picnic, that are placed over a shallow basin in order for you to dine while the water gently touches your feet. Though,
        be wary when using it as the base is slippery.<br><br>
        The beauty of Mimbalot Falls pours down a path of huge rocks, which in turn creates smaller rivers and miniature waterfalls out of it.
        The area directly under the waterfalls can be a major physical health hazard for swimming due to the large rocks and the strong force of the waterfall.<br>
        Shallow basins are found below the waterfall that are safe enough and location-wise, appropriate for swimming. Although, there are other deep, unnoticeable areas due to the whitewaters.
        If you want to go to the Falls, don't forget to bring your towel and extra clothing.<br><br>
        Whether you want to go to Mimbalot Falls and take a cold dip there, or go on an offroad adventure, the resort is welcome to offer you with such services.<br><br>
        If you want to find out where the resort is, open up Google Maps, and you will see the location pointed at Mimbalot Falls Road, Brgy. Buru-un, Iligan City.
        


